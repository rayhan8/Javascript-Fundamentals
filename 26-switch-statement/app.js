// Switch Statement

const dice = 4;
switch(dice) {
 case 1:
  console.log("you got one");
  break;
 case 2:
  console.log("you got two");
  break;
 case 3:
  console.log("you got three");
  break;
 case 4:
  console.log("you got four");
  break;
 case 5:
  console.log("you got five");
  break;
 case 6:
  console.log("you got six");
  break;
 default:
  console.log("you did not roll the dice");
}