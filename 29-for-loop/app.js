// Loops
// The code will continue as long as the conditions are true
// For Loop

let i;
for(i=0; i<10; i++) {
 console.log("Number is "+i);
}
// i=0; is Starting Point
// i<10; is End Point
// i++ means Increment

for(let number=11; number>=0; number--) {
 console.log("Number is "+number);
}
// number=11; is Starting Point
// number>=0; is End Point
// number-- means Decrement

for(let a=0; a<10; a=a+1) {
 console.log(a);
}