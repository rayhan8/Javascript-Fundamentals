// Numbers
// +=, -=, /=, *=, ++, --, %
// Modulus (%) operator returns the remainder after integer division

let number1 = 40;
number1 +=5;
console.log(number1);

let number2 = 40;
number2 -= 5;
console.log(number2);

number1 ++;
console.log(number1);

number2 --;
console.log(number2);

const slices = 10;
const children = 3;
const ammount = slices / children;
console.log(ammount);

// Modulus
const burger = 10;
const friend = 3;
const extra = burger % friend;
console.log(extra);

const random1 = 4+6+10*10;
console.log(random1);

const random2 = (4+6+10)*10;
console.log(random2);